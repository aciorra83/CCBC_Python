# Exercise 5: Checking the Type of a Value

# int
print(type(7))

# str
print(type("7"))

# Type conversion one example is done with the str() method
print(type(str(7)))

print(type(int("3")))

 
# Assigning variables
number = 7
print(number)

# Multiple assignment
a, b, c = 1, 2, 3
print(a)
print(b)
print(c)
